-- 9. TRANSACTIONS (2M+ rows)
CREATE OR REPLACE TABLE DEMO_DB.SHAKE_SHACK.MKT_TRANSACTIONS (
    TRANSACTION_ID      VARCHAR(36)     NOT NULL PRIMARY KEY,
    GUEST_ID            VARCHAR(36)     NOT NULL,
    LOCATION_ID         VARCHAR(20)     NOT NULL,
    TRANSACTION_DATE    DATE            NOT NULL,
    TRANSACTION_TIME    TIME,
    ORDER_CHANNEL       VARCHAR(30)     NOT NULL,
    ORDER_TOTAL         NUMBER(10,2)    NOT NULL,
    ITEM_COUNT          NUMBER(5,0),
    DISCOUNT_AMOUNT     NUMBER(10,2)    DEFAULT 0,
    PROMO_CODE          VARCHAR(30),
    DELIVERY_FEE        NUMBER(10,2)    DEFAULT 0,
    PLATFORM_COMMISSION NUMBER(10,2)    DEFAULT 0,
    NET_REVENUE         NUMBER(10,2),
    DAYPART             VARCHAR(20),
    IS_FIRST_VISIT      BOOLEAN         DEFAULT FALSE
);

-- 10. ORDER_ITEMS (6M+ rows)
CREATE OR REPLACE TABLE DEMO_DB.SHAKE_SHACK.MKT_ORDER_ITEMS (
    ORDER_ITEM_ID       VARCHAR(36)     NOT NULL PRIMARY KEY,
    TRANSACTION_ID      VARCHAR(36)     NOT NULL,
    MENU_ITEM_ID        VARCHAR(20)     NOT NULL,
    QUANTITY            NUMBER(5,0)     NOT NULL DEFAULT 1,
    UNIT_PRICE          NUMBER(10,2)    NOT NULL,
    LINE_TOTAL          NUMBER(10,2)    NOT NULL,
    IS_LTO_ITEM         BOOLEAN         DEFAULT FALSE,
    IS_UPSELL           BOOLEAN         DEFAULT FALSE
);
